#3.7/3。8字符串格式
s = "等级考试"
print("{0:*>25}Python语言".format(s))
# 输出：*********************等级考试Python语言

a= "等级考试"
b = "*"
c = 25
print("{0:{1}>{2}}Python语言".format(a,b,c))
# 输出：*********************等级考试Python语言


#数字的千位分隔符
print("{:,}".format(123456789))
# 输出：123,456,789

# .+数字 来表示精度（浮点数），或字符串的最大输出长达
print("{:.2f}".format(12345.46789))
# 输出：12345.47
print("{:.5}".format("Python是最接近人工智能的语言"))
# 输出：Pytho